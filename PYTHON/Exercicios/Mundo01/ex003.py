# Tipos primitivos (contas)
n1 = int(input('Escolha um número: '))
n2 = int(input('Escolha outro número: '))
soma = n1 + n2
menos = n1 - n2
vezes = n1 * n2
dividido = n1 / n2
print('A soma de {} com {} é igual a: {}'.format(n1, n2, soma))
print('A subtração de {} com {} é igual a: {}'.format(n1, n2, menos))
print('A multiplicação de {} com {} é igual a: {}'.format(n1, n2, vezes))
print('A divisão entre {} com {} é igual a: {}'.format(n1, n2, dividido))
